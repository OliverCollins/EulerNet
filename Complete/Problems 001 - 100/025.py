# 1000-digit Fibonacci number
# Solved

n_1, n, count = 0, 1, 1

# Loop while number of digits is less than 1000
while len(str(n)) < 1000:

	# Fibonacci sequence
	temp = n_1
	n_1 = n
	n += temp
	count += 1

print(count)
